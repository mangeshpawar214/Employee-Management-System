package com.Controller;

import java.util.ArrayList;

import com.Entity.Employee;
import com.Service.EmployeeService;

public class EmployeeController {

	EmployeeService service = new EmployeeService();

	public String saveEmployee(Employee employee) {
		String msg = service.saveEmployee(employee);
		return msg;
	}
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String updateEmployee(Employee employee) {

		String msg = service.updateEmployee(employee);
		return msg;

	}
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public Employee getById(int id) {
		Employee employee = service.getById(id);
		return employee;

	}
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public ArrayList<Employee> getAllEmployee() {
		ArrayList<Employee> employees = service.getAllemployee();

		return employees;

	}
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public ArrayList<Employee> getMaleEmployeesOnly() {
		return service.getMaleEmployeesOnly();
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public ArrayList<Employee> getFemaleEmployeesOnly() {
		return service.getFemaleEmployeesOnly();
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String deleteEmployeeById(int id) {
		String msg = service.deleteEmployeeById(id);
		return msg;

	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String deleteAllEmployee() {
		String msg = service.deleteAllEmployees();
		return msg;
	}

}
